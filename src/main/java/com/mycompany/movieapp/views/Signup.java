    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String username = jTextField1.getText().trim();
        String password1 = new String(jPasswordField1.getPassword());
        String password2 = new String(jPasswordField2.getPassword());
        
        if (username.isEmpty() || password1.isEmpty() || password2.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Vui lòng nhập đầy đủ thông tin.",
                    "Thiếu thông tin",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        if (!password1.equals(password2)) {
            JOptionPane.showMessageDialog(this,
                    "Mật khẩu nhập lại không khớp.",
                    "Lỗi",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (UserService.findByUsername(username) != null) {
            JOptionPane.showMessageDialog(this,
                    "Tài khoản đã tồn tại, vui lòng chọn tên khác.",
                    "Trùng tên đăng nhập",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        int newUserId = UserService.getAllUsers().size() + 1;
        Customer newCustomer = new Customer(newUserId, username, password1,
                username + "@example.com", "", "");
        UserService.addUser(newCustomer);
        
        JOptionPane.showMessageDialog(this,
                "Đăng ký thành công! Bạn có thể đăng nhập ngay.",
                "Thành công",
                JOptionPane.INFORMATION_MESSAGE);
        
        jTextField1.setText("");
        jPasswordField1.setText("");
        jPasswordField2.setText("");

        Login loginFrame = new Login(); 
        loginFrame.setDefaultCloseOperation(javax.swing.JFrame.EXIT_ON_CLOSE);
        loginFrame.setLocationRelativeTo(null); 
        loginFrame.setVisible(true);
        
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

